class MathExecutor {
    func add(_ number1: Int, _ number2: Int) -> Int {
        return number1 + number2
    }
}

class StringExecutor {
    func add(_ string1: String, _ string2: String) -> String {
        return string1 + string2
    }
}

class BoolExecutor {
    func add(_ bool1: Bool, _ bool2: Bool) -> Bool {
        return bool1 && bool2
    }
}
